package com.meitianhui.supplierCentre.entity;

import java.io.Serializable;

public class AuditFlow implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -6797215732136029231L;
	private String flow_id;//流程标识
	private String supplier_id;//供应商id
	private String action_by;//操作人id
	private String action_date;//审批日期
	private String is_current_step;//当前处理阶段，Y（是）N（否）
	private String is_closed;//流程是否完成，Y（是）N（否）
	private String result;//处理结果，可选值：trial（初审）sample（寄样）review（复审）pass（通过）fail（驳回）
	private String created_date;//创建日期
	private String remark;//备注
	private String responsible; //操作人
	private String organization;//组织机构
	public String getResponsible() {
		return responsible;
	}
	public void setResponsible(String responsible) {
		this.responsible = responsible;
	}
	public String getOrganization() {
		return organization;
	}
	public void setOrganization(String organization) {
		this.organization = organization;
	}
	public String getFlow_id() {
		return flow_id;
	}
	public void setFlow_id(String flow_id) {
		this.flow_id = flow_id;
	}
	public String getSupplier_id() {
		return supplier_id;
	}
	public void setSupplier_id(String supplier_id) {
		this.supplier_id = supplier_id;
	}
	public String getAction_by() {
		return action_by;
	}
	public void setAction_by(String action_by) {
		this.action_by = action_by;
	}
	public String getAction_date() {
		return action_date;
	}
	public void setAction_date(String action_date) {
		this.action_date = action_date;
	}
	public String getIs_current_step() {
		return is_current_step;
	}
	public void setIs_current_step(String is_current_step) {
		this.is_current_step = is_current_step;
	}
	public String getIs_closed() {
		return is_closed;
	}
	public void setIs_closed(String is_closed) {
		this.is_closed = is_closed;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	public String getCreated_date() {
		return created_date;
	}
	public void setCreated_date(String created_date) {
		this.created_date = created_date;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}



}
