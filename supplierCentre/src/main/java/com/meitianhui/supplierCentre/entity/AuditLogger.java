package com.meitianhui.supplierCentre.entity;

import java.io.Serializable;

public class AuditLogger implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 6750333412637806427L;
	private String log_id;//日志标识
	private String supplier_id;//供应商标识
	private String category;//事件类别
	private String tracked_date;//发生时间
	private String event_desc;//事件描述
	private String responsible;//负责人
	private String organization;//组织机构
	private String remark;//备注
	
	public String getResponsible() {
		return responsible;
	}
	public void setResponsible(String responsible) {
		this.responsible = responsible;
	}
	public String getOrganization() {
		return organization;
	}
	public void setOrganization(String organization) {
		this.organization = organization;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getLog_id() {
		return log_id;
	}
	public void setLog_id(String log_id) {
		this.log_id = log_id;
	}
	public String getSupplier_id() {
		return supplier_id;
	}
	public void setSupplier_id(String supplier_id) {
		this.supplier_id = supplier_id;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getTracked_date() {
		return tracked_date;
	}
	public void setTracked_date(String tracked_date) {
		this.tracked_date = tracked_date;
	}
	public String getEvent_desc() {
		return event_desc;
	}
	public void setEvent_desc(String event_desc) {
		this.event_desc = event_desc;
	}
}
